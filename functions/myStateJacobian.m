function [A, Bmv] = myStateJacobian(X,U)

H1max = 0.35; %m
a = 0.25; %m
w = 0.035; %m
H2max = 0.35; %m
b = 0.345; %m
c = 0.1; %m
H3max = 0.35; %m
R = 0.364; %m
params = zeros(4,1);

A11 = U(2)/(2*X(1)^(1/2)*a*w) - params(1)/(2*X(1)^(1/2)*a*w);
A12 = 0;
A13 = 0;
A21 = U(2)/(2*X(1)^(1/2)*(c*w+X(2)/H2max*b*w)) + params(1)/(2*X(1)^(1/2)*(c*w+X(2)/H2max*b*w));
A22 = -U(2)*X(1)^(1/2)*b*w/H2max/(c*w+X(2)/H2max*b*w)^2 - (U(3)/(2*X(2)^(1/2))*(c*w+X(2)/H2max*b*w)-U(3)*X(2)^(1/2)*b*w/H2max)/(c*w+X(2)/H2max*b*w)^2 - params(1)*X(1)^(1/2)*b*w/H2max/(c*w+X(2)/H2max*b*w)^2 - (params(4)/(2*X(2)^(1/2))*(c*w+X(2)/H2max*b*w)-params(4)*X(2)^(1/2)*b*w/H2max)/(c*w+X(2)/H2max*b*w)^2;
A23 = 0;
A31 = 0;
A32 = U(3)/(2*X(2)^(1/2))/(w*(R^2-(R-X(3))^2)^(1/2)) + params(2)/(2*X(2)^(1/2))/(w*(R^2-(R-X(3))^2)^(1/2));
A33 = -U(3)*X(2)^(1/2)*w*(R-X(3))/(R^2-(R-X(3))^2)^(1/2)/(w*(R^2-(R-X(3))^2)^(1/2))^2 - (U(4)/(2*X(3)^(1/2))*(w*(R^2-(R-X(3))^2)^(1/2))-U(4)*X(3)^(1/2)*w*(R-X(3))/(R^2-(R-X(3))^2)^(1/2))/(w*(R^2-(R-X(3))^2)^(1/2))^2 - params(2)*X(2)^(1/2)*w*(R-X(3))/(R^2-(R-X(3))^2)^(1/2)/(w*(R^2-(R-X(3))^2)^(1/2))^2 - (params(3)/(2*X(3)^(1/2))*(w*(R^2-(R-X(3))^2)^(1/2))-params(3)*X(3)^(1/2)*w*(R-X(3))/(R^2-(R-X(3))^2)^(1/2))/(w*(R^2-(R-X(3))^2)^(1/2))^2;
A = [A11 A12 A13; A21 A22 A23; A31 A32 A33];

Bmv11 = 1/(a*w);
Bmv12 = -X(1)^(1/2)/(a*w);
Bmv13 = 0;
Bmv14 = 0;
Bmv21 = 0;
Bmv22 = X(1)^(1/2)/(c*w+X(2)/H2max*b*w);
Bmv23 = -X(2)^(1/2)/(c*w+X(2)/H2max*b*w);
Bmv24 = 0;
Bmv31 = 0;
Bmv32 = 0;
Bmv33 = X(2)^(1/2)/(w*(R^2-(R-X(3))^2)^(1/2));
Bmv34 = -X(3)^(1/2)/(w*(R^2-(R-X(3))^2)^(1/2));
Bmv = [Bmv11 Bmv12 Bmv13 Bmv14; Bmv21 Bmv22 Bmv23 Bmv24; Bmv31 Bmv32 Bmv33 Bmv34];

end
